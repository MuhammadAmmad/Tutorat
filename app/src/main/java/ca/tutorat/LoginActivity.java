    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;

        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            try {
                // Simulate network access.
                Thread.sleep(2000);
                mAuth.signInWithEmailAndPassword(mEmail, mPassword).addOnCompleteListener(new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        if (task.isSuccessful()) {
                            // onPostExecute() method is called.
                        } else {
                            // Error login I will implement this later.
                            Toast.makeText(LoginActivity.this, "Error login", Toast.LENGTH_SHORT).show();
                        }
                    }
                });
            } catch (InterruptedException e) {
                return false;
            }

            // TODO: register the new account here.
            return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                // go to the student Main page if userType == 1
                DatabaseReference currentUser;
                if (userType == STUDENT) {
                    currentUser = refDatabase.child("STUDENT");
                } else {
                    currentUser = refDatabase.child("TUTOR");
                }
                final String currentUserId = mAuth.getCurrentUser().getUid();

                currentUser.addValueEventListener(new ValueEventListener() {
                        @Override
                        public void onDataChange(DataSnapshot dataSnapshot) {
                            Intent intent;
                            if (dataSnapshot.hasChild(currentUserId)) {
                                if (userType == STUDENT) {
                                    intent = new Intent(LoginActivity.this, StudentMainPage.class);

                                } else {
                                    intent = new Intent(LoginActivity.this, TutorMainPage.class);
                                }

                                intent.putExtra("connexion", CONNEXION);
                                //To pass:
                                intent.putExtra("Student", user);
                                startActivity(intent);
                                finish();
                            } else {
                                Toast.makeText(LoginActivity.this, "You need to setup your account", Toast.LENGTH_SHORT).show();
                            }
                        }

                        @Override
                        public void onCancelled(DatabaseError databaseError) {

                        }
                    });

                finish();
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
